#include "stm32f10x.h"
#include "../driver/spi.h"
#include "../driver/mfrc522.h"
/******************************************************************************/
/*                     EXPORTED TYPES and DEFINITIONS                         */
/******************************************************************************/

/******************************************************************************/
/*                              PRIVATE DATA                                  */
/******************************************************************************/
uint8_t byCardID[5];
char cBuff[100];
/******************************************************************************/
/*                              EXPORTED DATA                                 */
/******************************************************************************/

/******************************************************************************/
/*                            PRIVATE FUNCTIONS                               */
/******************************************************************************/

/******************************************************************************/
/*                            EXPORTED FUNCTIONS                              */
/******************************************************************************/

/******************************************************************************/
int main(void)
{
    spiInit();
    mfrc522Init();
    while (1)
    {
        if (mfrc522Check(byCardID) == MI_OK)
        {
            sprintf(cBuff, "ID: 0x%02X%02X%02X%02X%02X", byCardID[0], byCardID[1], byCardID[2], byCardID[3], byCardID[4]);
        }
    }
}
